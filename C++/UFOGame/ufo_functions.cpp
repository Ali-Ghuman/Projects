void end_game(std::string answer, std::string codeword)  //checks if the user gets the word after all the tries have expired 
{
  if (answer == codeword)
  {
    std::cout << "\nHooray! You saved the person and earned a medal of honor!\n";
    std::cout << "Codeword: " << codeword << "\n"; 
  } 
  else 
  {
    std::cout << "\nOh no! The UFO just flew away with another person!\n"; 
    std::cout << "Codeword: " << codeword << "\n"; 
  }
}

